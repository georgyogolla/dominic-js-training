// Switch statements
let month = 13;

switch(month){
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    case 6:
        console.log("June");
        break;
    case 7:
        console.log("July");
        break;
    case 8:
        console.log("August");
        break;
    case 9:
        console.log("September");
        break;
    case 10:
        console.log("October");
        break;
    case 11:
        console.log("November");
        break;
    case 12:
        console.log("December");
        break;
    default:
        console.log("Invalid month");
}

// use switch statement with weather variables
let weather = "Coldy";

switch(weather){
    case "Sunny":
        console.log("It's a beautiful day!");
        break;
    case "Rainy":
        console.log("It's raining!");
        break;
    case "Cloudy":
        console.log("It's a bit cloudy.");
        break;
    default:
        console.log("Unknown weather condition");
}